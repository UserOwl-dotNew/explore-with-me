package ru.practicum.mainservice.events.service;

import ru.practicum.common.enums.EventState;
import ru.practicum.mainservice.events.dto.*;
import ru.practicum.mainservice.events.entity.Event;

import java.time.LocalDateTime;
import java.util.List;

public interface EventService {

    // Admin

    /**
     * Admin получает список событий с фильтрацией
     * <p>
     * Возвращает полную информацию обо всех событиях, подходящих под переданные условия.
     * В случае, если по заданным фильтрам не найдено ни одного события, возвращает пустой список.
     * </p>
     *
     * @param users      список id пользователей, чьи события нужно найти (необязательный)
     * @param states     список состояний, в которых находятся искомые события (необязательный)
     * @param categories список id категорий, в которых будет вестись поиск (необязательный)
     * @param rangeStart дата и время, не раньше которых должно произойти событие (необязательный)
     * @param rangeEnd   дата и время, не позже которых должно произойти событие (необязательный)
     * @param from       количество событий, которые нужно пропустить для формирования текущего набора (по умолчанию 0)
     * @param size       количество событий в наборе (по умолчанию 10)
     * @return список событий с полной информацией
     */
    List<EventFullDto> getAdminEvents(List<Long> users, List<EventState> states,
                                      List<Long> categories, LocalDateTime rangeStart,
                                      LocalDateTime rangeEnd, int from, int size);

    /**
     * Admin редактирует данные события и его статус (отклонение/публикация)
     * <p>
     * Редактирование данных любого события администратором.
     * <br>Обратите внимание:
     * <ul>
     *   <li>дата начала изменяемого события должна быть не ранее чем за час от даты публикации (код 409)</li>
     *   <li>событие можно публиковать, только если оно в состоянии ожидания публикации (код 409)</li>
     *   <li>событие можно отклонить, только если оно еще не опубликовано (код 409)</li>
     * </ul>
     * </p>
     *
     * @param eventId id редактируемого события
     * @param request данные для изменения информации о событии
     * @return обновленное событие с полной информацией
     * @throws ru.practicum.common.exception.NotFoundException если событие не найдено
     * @throws ru.practicum.common.exception.ConflictException если событие не удовлетворяет правилам редактирования
     */
    EventFullDto updateAdminEvent(Long eventId, UpdateEventAdminRequest request);

    // PRIVATE

    /**
     * Private: Получение событий, добавленных текущим пользователем
     * <p>
     * Возвращает краткую информацию о всех событиях, созданных пользователем.
     * В случае, если по заданным фильтрам не найдено ни одного события, возвращает пустой список.
     * </p>
     *
     * @param userId id текущего пользователя
     * @param from   количество элементов, которые нужно пропустить для формирования текущего набора (по умолчанию 0)
     * @param size   количество элементов в наборе (по умолчанию 10)
     * @return список событий с краткой информацией
     */
    List<ru.practicum.common.dto.EventShortDto> getUserEvents(Long userId, int from, int size);

    /**
     * Private: Добавление нового события
     * <p>
     * Создает новое событие от имени пользователя.
     * <br>Обратите внимание: дата и время, на которые намечено событие,
     * не может быть раньше, чем через два часа от текущего момента.
     * </p>
     *
     * @param userId id текущего пользователя
     * @param dto    данные добавляемого события
     * @return созданное событие с полной информацией (статус PENDING)
     * @throws ru.practicum.common.exception.BadRequestException если запрос составлен некорректно
     * @throws ru.practicum.common.exception.ConflictException   если событие не удовлетворяет правилам создания
     * @throws ru.practicum.common.exception.NotFoundException   если категория не найдена
     */
    EventFullDto createEvent(Long userId, NewEventDto dto);

    /**
     * Private: Получение полной информации о событии, добавленном текущим пользователем
     * <p>
     * Возвращает полную информацию о событии, принадлежащем пользователю.
     * В случае, если события с заданным id не найдено, возвращает статус код 404.
     * </p>
     *
     * @param userId  id текущего пользователя
     * @param eventId id события
     * @return полная информация о событии
     * @throws ru.practicum.common.exception.NotFoundException если событие не найдено или не принадлежит пользователю
     */
    EventFullDto getUserEvent(Long userId, Long eventId);

    /**
     * Private: Изменение события, добавленного текущим пользователем
     * <p>
     * Редактирует событие, созданное пользователем.
     * <br>Обратите внимание:
     * <ul>
     *   <li>изменить можно только отмененные события или события в состоянии ожидания модерации (код 409)</li>
     *   <li>дата и время, на которые намечено событие, не может быть раньше, чем через два часа от текущего момента (код 409)</li>
     * </ul>
     * </p>
     *
     * @param userId  id текущего пользователя
     * @param eventId id редактируемого события
     * @param request новые данные события
     * @return обновленное событие с полной информацией
     * @throws ru.practicum.common.exception.NotFoundException   если событие не найдено или не принадлежит пользователю
     * @throws ru.practicum.common.exception.ConflictException   если событие не удовлетворяет правилам редактирования
     * @throws ru.practicum.common.exception.BadRequestException если запрос составлен некорректно
     */
    EventFullDto updateUserEvent(Long userId, Long eventId, UpdateEventUserRequest request);

    // PUBLIC

    /**
     * Public: Получение событий с возможностью фильтрации
     * <p>
     * Возвращает список опубликованных событий с возможностью фильтрации.
     * <br>Обратите внимание:
     * <ul>
     *   <li>в выдаче должны быть только опубликованные события</li>
     *   <li>текстовый поиск (по аннотации и подробному описанию) должен быть без учета регистра букв</li>
     *   <li>если в запросе не указан диапазон дат [rangeStart-rangeEnd], то нужно выгружать события,
     *       которые произойдут позже текущей даты и времени</li>
     *   <li>информация о каждом событии должна включать в себя количество просмотров
     *       и количество уже одобренных заявок на участие</li>
     *   <li>информацию о том, что по этому эндпоинту был осуществлен и обработан запрос,
     *       нужно сохранить в сервисе статистики</li>
     * </ul>
     * В случае, если по заданным фильтрам не найдено ни одного события, возвращает пустой список.
     * </p>
     *
     * @param text          текст для поиска в содержимом аннотации и подробном описании события (необязательный, от 1 до 7000 символов)
     * @param categories    список идентификаторов категорий, в которых будет вестись поиск (необязательный)
     * @param paid          поиск только платных/бесплатных событий (необязательный)
     * @param rangeStart    дата и время, не раньше которых должно произойти событие (необязательный)
     * @param rangeEnd      дата и время, не позже которых должно произойти событие (необязательный)
     * @param onlyAvailable только события, у которых не исчерпан лимит запросов на участие (по умолчанию false)
     * @param sort          вариант сортировки: EVENT_DATE - по дате события, VIEWS - по количеству просмотров (необязательный)
     * @param from          количество событий, которые нужно пропустить для формирования текущего набора (по умолчанию 0)
     * @param size          количество событий в наборе (по умолчанию 10)
     * @return список событий с краткой информацией, включая количество просмотров и подтвержденных заявок
     * @throws ru.practicum.common.exception.BadRequestException если запрос составлен некорректно
     */
    List<ru.practicum.common.dto.EventShortDto> getPublicEvents(String text, List<Long> categories,
                                                                Boolean paid, LocalDateTime rangeStart,
                                                                LocalDateTime rangeEnd, Boolean onlyAvailable,
                                                                String sort, int from, int size);

    /**
     * Public: Получение подробной информации об опубликованном событии по его идентификатору
     * <p>
     * Возвращает полную информацию об опубликованном событии.
     * <br>Обратите внимание:
     * <ul>
     *   <li>событие должно быть опубликовано</li>
     *   <li>информация о событии должна включать в себя количество просмотров
     *       и количество подтвержденных запросов</li>
     *   <li>информацию о том, что по этому эндпоинту был осуществлен и обработан запрос,
     *       нужно сохранить в сервисе статистики</li>
     * </ul>
     * В случае, если события с заданным id не найдено, возвращает статус код 404.
     * </p>
     *
     * @param eventId id события
     * @return полная информация о событии с просмотрами и количеством подтвержденных заявок
     * @throws ru.practicum.common.exception.NotFoundException   если событие не найдено или не опубликовано
     * @throws ru.practicum.common.exception.BadRequestException если запрос составлен некорректно
     */
    EventFullDto getPublicEvent(Long eventId);

    // ==================== INTERNAL (для других сервисов) ====================

    /**
     * Internal: Получение сущности события по идентификатору
     * <p>
     * Используется для внутренних вызовов между сервисами.
     * Возвращает JPA-сущность для дальнейшей обработки.
     * </p>
     *
     * @param eventId id события
     * @return сущность события
     * @throws ru.practicum.common.exception.NotFoundException если событие не найдено
     */
    Event getEventEntity(Long eventId);

    /**
     * Internal: Проверка существования события по идентификатору
     * <p>
     * Используется для валидации в других сервисах (например, в сервисе запросов или подборок).
     * </p>
     *
     * @param eventId id события
     * @return true - если событие существует, false - если не найдено
     */
    boolean existsById(Long eventId);

    /**
     * Internal: Получение списка сущностей событий по списку идентификаторов
     * <p>
     * Используется для проверки существования событий при создании/обновлении подборок.
     * </p>
     *
     * @param eventIds список id событий
     * @return список сущностей событий (только существующие)
     */
    List<Event> findAllByIds(List<Long> eventIds);
}